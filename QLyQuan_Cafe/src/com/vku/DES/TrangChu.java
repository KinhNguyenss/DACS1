    private void taiTrang(int i) {
        bg.removeAll();
        switch (i) {
            case 1:
                if (bhc == null) {
                    bhc = new BanHang_Chu();
                }
                bg.add(bhc);

                break;
            case 2:
                if (ke == null) {
                    ke = new Thong_Ke();
                }
                bg.add(ke);

                break;
            case 3:
                if (qlc == null) {
                    qlc = new QLChu();
                }
                bg.add(qlc);

                break;
            case 4:
                if (tl == null) {
                    tl = new ThietLap();
                }
                bg.add(tl);

                break;
            default:

                break;
        }
        bg.updateUI();
    }

   
   
